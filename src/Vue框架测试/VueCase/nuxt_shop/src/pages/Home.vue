<template>
    <div>
      <el-button type="info" @click="logout">退出</el-button>
    </div>
  </template>
  
  <script>
  export default {
    methods: {
      logout() {
        // 清除token
        window.sessionStorage.clear()
        this.$router.push('/login') //回退到登录页
      }
    }
  }
  </script>
  
